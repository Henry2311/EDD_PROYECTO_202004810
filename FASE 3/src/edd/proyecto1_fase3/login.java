    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String user = this.jTextField1.getText();
        String pass = this.jPasswordField1.getText();
        
        if(user.equals("admin") && pass.equals("EDD2022")){
            new admin().setVisible(true);
            this.dispose();
        }else{
            Clientes cliente = EDDProyecto1_fase3.CLIENTES.search(user);
            if(cliente != null){
                if(cliente.getUser().equals(user) && cliente.getPassword().equals(pass)){
                    new ModCliente(cliente).setVisible(true);
                    this.dispose();
                }
            }else{
                JOptionPane.showMessageDialog(this, "Cliente no registrado");
            }
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

